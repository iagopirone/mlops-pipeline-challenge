import argparse
import json
from pathlib import Path
from typing import Any


ROOT_DIR = Path(__file__).resolve().parents[2]
RESULTS_FILE = ROOT_DIR / "storage" / "inference_results" / "results.jsonl"


def load_inference_result(inference_id: str) -> dict[str, Any] | None:
    if not RESULTS_FILE.exists():
        return None

    with RESULTS_FILE.open("r", encoding="utf-8") as file:
        for line in file:
            if not line.strip():
                continue

            result = json.loads(line)

            if result.get("inference_id") == inference_id:
                return result

    return None


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Recover an inference result by inference_id."
    )
    parser.add_argument(
        "--inference-id",
        required=True,
        help="Inference id generated by the inference worker.",
    )

    args = parser.parse_args()

    result = load_inference_result(args.inference_id)

    if result is None:
        print(
            json.dumps(
                {
                    "status": "not_found",
                    "inference_id": args.inference_id,
                    "results_file": str(RESULTS_FILE),
                },
                indent=2,
            )
        )
        return

    print(json.dumps(result, indent=2))


if __name__ == "__main__":
    main()